for (const lang of ["nl", "en", "fr"]) {
  Object.assign(I18N_TRANSLATIONS[lang], EXTRA_I18N_TRANSLATIONS[lang] || {});
}

function getCurrentLanguage() {
  const stored = localStorage.getItem("siteLanguage");
  if (stored === "en" || stored === "nl" || stored === "fr") return stored;
  return "nl";
}

function setCurrentLanguage(lang) {
  localStorage.setItem("siteLanguage", lang);
}

function t(key, lang) {
  return I18N_TRANSLATIONS[lang]?.[key] ?? I18N_TRANSLATIONS.nl[key] ?? key;
}

function applyTranslations(lang) {
  document.documentElement.lang = lang;

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    element.textContent = t(key, lang);
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
    const key = element.dataset.i18nPlaceholder;
    element.setAttribute("placeholder", t(key, lang));
  });
}

function syncLanguageButtons(lang) {
  document.querySelectorAll(".lang-btn").forEach((button) => {
    button.classList.toggle("is-active", button.dataset.lang === lang);
  });
}

function setupLanguageSwitcher() {
  const buttons = document.querySelectorAll(".lang-btn");
  if (!buttons.length) return;

  buttons.forEach((button) => {
    button.addEventListener("click", () => {
      const lang = button.dataset.lang;
      if (lang !== "nl" && lang !== "en" && lang !== "fr") return;
      setCurrentLanguage(lang);
      applyTranslations(lang);
      syncLanguageButtons(lang);
      document.dispatchEvent(new CustomEvent("languageChanged", { detail: { lang } }));
    });
  });
}

const initialLanguage = getCurrentLanguage();
applyTranslations(initialLanguage);
setupLanguageSwitcher();
syncLanguageButtons(initialLanguage);

window.getCurrentLanguage = getCurrentLanguage;
window.i18nTranslate = t;
